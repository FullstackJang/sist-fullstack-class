/*
 	기본형 데이터형을 객체로 감싸서 여러가지 기능 사용할 수 있는
 	Wrapper Class의 사용
*/

public class UseWrapperClass {

	public static void main(String[] args) {
		
		//상수의 사용
		System.out.println("byte의 최소값: " + Byte.MIN_VALUE + ", 최대값:" + Byte.MAX_VALUE );
		
		//기능의 사용
		
		
		int a = 4;
		
		System.out.println(~a);
		
	}// main
}// class
